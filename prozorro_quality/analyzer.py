def extract_document_review(parsed_documents: list[ParsedDocument]) -> dict[str, list[dict[str, Any]]]:
    from typing import Any
    import re
    
    review_data = {
        "requirements": [],
        "deadlines": [],
        "qualification": [],
        "evaluation": [],
        "documents": [],
    }
    
    compiled_patterns = {}
    for key, patterns in REVIEW_HEURISTICS.items():
        compiled_patterns[key] = [re.compile(p, re.UNICODE) for p in patterns]
        
    seen_quotes = set()
    
    for parsed in parsed_documents:
        text = parsed.text
        if not text:
            continue
            
        lines = []
        for block in re.split(r'\n+', text):
            block = block.strip()
            if not block:
                continue
            sentences = re.split(r'(?<=[.!?])\s+', block)
            for s in sentences:
                s = s.strip()
                if len(s) > 15:
                    lines.append(s)
                    
        for sentence in lines:
            clean_sentence = re.sub(r'\s+', ' ', sentence)
            norm_sentence = clean_sentence.lower().strip()
            if norm_sentence in seen_quotes:
                continue
                
            matched = False
            for key, regexes in compiled_patterns.items():
                for rx in regexes:
                    if rx.search(clean_sentence):
                        seen_quotes.add(norm_sentence)
                        review_data[key].append({
                            "quote": clean_sentence,
                            "document_title": parsed.document.title,
                            "document_id": parsed.document.id,
                        })
                        matched = True
                        break
                if matched:
                    break
                        
    for key in review_data:
        top_entries = sorted(review_data[key], key=lambda x: len(x["quote"]), reverse=True)[:15]
        review_data[key] = sorted(top_entries, key=lambda x: x["document_title"])
        
    return review_data
